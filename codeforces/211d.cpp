int main() {
	//ios::sync_with_stdio(false);
	ll N,M,R;
	while(cin>>N>>M>>R) {
		VI ns(N),ms(M);
		REP(i, N) {
			cin>>ns[i];
		}
		REP(i, M) {
			cin>>ms[i];
		}
		int ni=0;
		int ans=0;
		int pay=0;
		sort(ALL(ns));
		sort(ALL(ms));
//		cout<<ns<<endl;
//		cout<<ms<<endl;
		REP(i, M) {
			if(ans>=N) break;
			while(ni<N) {
				if(ns[ni] + R >= ms[i]) {
					int add = max(0LL, ms[i] - ns[ni]);
					R-=add;
					assert(R>=0);
//					cout<<i<<" add "<<ns[ni]<<" "<<add<<" -> R "<<R<<endl;
					ans++;
					pay+=min(ns[ni], ms[i]);
					ni++;
					break;
				}
				ni++;
			}
		}
//		cout<<R<<endl;
		pay = max(0LL, pay-R);
		cout<<ans<<" "<<pay<<endl;
	}
	
	return 0;
}
